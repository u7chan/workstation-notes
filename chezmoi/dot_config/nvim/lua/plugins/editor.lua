local function tree_copy_path(mode)
  local api = require("nvim-tree.api")
  local node = api.tree.get_node_under_cursor()
  if not node or not node.absolute_path then
    return
  end

  local path = node.absolute_path
  if mode == "relative" then
    path = vim.fn.fnamemodify(path, ":.")
  end

  vim.fn.setreg("+", path)
  print(path)
end

local function tree_path_menu()
  vim.ui.select({
    { label = "relative path をコピー", mode = "relative" },
    { label = "absolute path をコピー", mode = "absolute" },
  }, {
    prompt = "nvim-tree path",
    format_item = function(item)
      return item.label
    end,
  }, function(choice)
    if choice then
      tree_copy_path(choice.mode)
    end
  end)
end

